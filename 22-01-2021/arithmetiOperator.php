<?php
$no1 = 10;
$no2 = 20;
$no3 = 2;

// Addition operator
$sum = $no1 + $no2;
echo $sum."<br>";

//Subtraction
$sub = $no2 - $no1;
echo $sub."<br>";

// Multiplication
$mul = $no1 * $no2;
echo $mul."<br>";

// Division
$div = $no2 / $no1;
echo $div."<br>";

// Mixture of operators
$result = $no1 + $no2 / $no3;
echo $result."<br>";

$result1 = ($no1 + $no2) / $no3;
echo $result1."<br>";

// Increment operator
$no1++;
echo $no1."<br>";

// decrement operator
$no2--;
echo $no2."<br>";

//Modulus
$mod = $no2 % $no3;
echo $mod."<br>";